""" -------------------------------------------------------------
| 'for' é uma estrutura de repetição muito parecido com 'while' |
| com a diferença em que nele você pode definir o numero de     |
| repetições que deseja executar.                               |
--------------------------------------------------------------"""

for i in range(2): # 'for' atribui a variavel 'i' os valores que estão em 'range(2)'
    print(i)
    print("Matheus")

